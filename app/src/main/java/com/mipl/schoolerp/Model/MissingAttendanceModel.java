package com.mipl.schoolerp.Model;

public class MissingAttendanceModel {

    String Id,Name;

    public MissingAttendanceModel(String id, String name) {
        Id = id;
        Name = name;
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
